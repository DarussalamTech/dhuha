        <h1>TEACHING SYSTEM</h1>
        <p><span>DHUHA</span> believes that an intelligent approach produces better results. Excellent results are achieved by:</p>
        <ul>
            <li>Planning and Preparing</li>
            <li>Teaching methodology and education system</li>
            <li>Conducting frequent and regular testing</li>
            <li>Continuous and appropriate follow - up</li>
            <li>Encouraging active student and parents participation in the education process.</li>
        </ul>
        <p>Everything possible is done to ensure success, and no student is allowed to fail easily.</p>
        <h2>Planning and preparing:</h2>
        <p>Thorough preparation precedes teaching. Each course is exhaustively researched and structured and is described in terms of hundreds of clearly stated objectives, eliminating guesswork. A plan for each subject at each stage is prepared. Each plan is complete which includes all of the points to be taught and all the skills to be developed. By using the efficient teaching methods and by following the pacing charts, teachers implement the staged plans, hereby helping students to fulfill their potential.</p>
        <h2>Teaching methods :</h2>
        <p>The whole class is taught together. Class time is utilized by building a solid foundation of knowledge through effective discussion based on critical thinking and logical reasoning. The varied teaching methods used and pace set simulate students.</p>
        <h2>Testing &amp; Monitoring:</h2>
        <p>Continues evaluation of student learning is an integral part of the instructional process at DHUHA. Testing to evaluate student learning, and to permit timely positive intervention, is frequent and regular. Testing students in this way has a number of <span>important advantages:</span></p>
        <ul>
            <li>Students are prepared to perform well under the pressure of time constrains</li>
            <li>Students develop the ability to focus and sustain concentration</li>
            <li>Feedback from testing allows close monitoring and follow up</li>
        </ul>
        <p>The regular testing allows “gaps” in student knowledge to be pinpointed as they form. Regular testing allows the progress of each and every student to be continuously monitored and problem areas to be rectified.</p>
        <h2>Follow-Up:</h2>
        <p>The progress of each student is continuously followed up and all students receive the support needed. Every available means is used to help those who fall behind to catch up. They are advised in study techniques,
            Motivated by the academic staff and encouraged by receiving help from peer-tutors or teachers. Also regular parent-teacher meetings are held to keep the parents in picture.</p>
        
